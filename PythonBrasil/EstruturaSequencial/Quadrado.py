# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário
# Ivo Dias

# Recebe o lado
lado = int(input("Informe o valor do lado: "))

# Calcula a area
area = lado * lado
# Calcula o dobro da area
dobro = 2 * area

# Mostra na tela
print("O dobro da area é",dobro)